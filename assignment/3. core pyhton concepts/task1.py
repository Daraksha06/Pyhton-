# Write a Python program to demonstrate the creation of variables and different data types. 
# Numeric Types
integer_num = 42
float_num = 3.14
complex_num = 2 + 3j

# Sequence Types
string_text = "Hello, Python!"
list_data = [1, 2, 3, "a"]
tuple_data = (1, 2, 3, "b")
range_data = range(5)

# Set Types
set_data = {1, 2, 3}

# Mapping Type
mydict= {"name": "DARAKSHA", "age": 21}

# Boolean
bool_value = True

# None Type
none_value = None

print("Integer:", integer_num)
print("Float:", float_num)
print("Complex:", complex_num)
print("String:", string_text)
print("List:", list_data)
print("Tuple:", tuple_data)
print("Range:", list(range_data))  # Converting it  to list to print values
print("Set:", set_data)
print("Dictionary:", mydict)
print("Boolean:", bool_value)
print("None:", none_value)

