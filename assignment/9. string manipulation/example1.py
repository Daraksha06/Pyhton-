# Write a Python program to demonstrate string slicing.
mystr = "Python Programming"

print(mystr[0:6])
print(mystr[:6])
print(mystr[7:])
print(mystr[:])
print(mystr[::2])
print(mystr[::-1])
print(mystr[-11:-1])
print(mystr[-1:-12:-2])
print(mystr[-6:-1:1])
print(mystr[::-1])
print(mystr[-7:-1])
print(mystr[::3])
